import ants
import numpy as np
import time
import os
import sys

# Add src to path
sys.path.insert(0, os.path.abspath(os.path.join(os.path.dirname(__file__), 'src')))
from syntx.syn import registration

def compute_tissue_overlap(fixed_img, warped_img):
    fixed_seg = ants.threshold_image(fixed_img, 'Otsu', 3)
    warped_seg = ants.threshold_image(warped_img, 'Otsu', 3)
    overlap = ants.label_overlap_measures(fixed_seg, warped_seg)
    if 'MeanOverlap' in overlap.columns:
        return float(overlap.loc[overlap['Label'] == 'All', 'MeanOverlap'].values[0])
    return 0.0

def main():
    print("Loading phantom images...")
    r16 = ants.image_read(ants.get_data('r16'))
    r27 = ants.image_read(ants.get_data('r27'))
    r64 = ants.image_read(ants.get_data('r64'))
    
    pairs = [
        ('r16', 'r27', r16, r27),
        ('r16', 'r64', r16, r64),
        ('r27', 'r64', r27, r64)
    ]
    
    print("Evaluating ANTs registration baseline...")
    ants_results = {}
    ants_dices = []
    for name1, name2, f, m in pairs:
        res = ants.registration(fixed=f, moving=m, type_of_transform='SyN')
        dice = compute_tissue_overlap(f, res['warpedmovout'])
        ants_dices.append(dice)
        ants_results[(name1, name2)] = dice
        print(f"ANTs ({name1}, {name2}): {dice:.4f}")
    mean_ants_dice = np.mean(ants_dices)
    print(f"Mean ANTs Dice: {mean_ants_dice:.4f}\n")
    
    # We will run a set of selected configurations to check parity.
    # We want to identify a configuration that achieves mean DICE within 1% of mean_ants_dice (i.e. mean_ants_dice - 0.01)
    configs = [
        # (levels, reg_its, aff_its, grad_step, flow_sigma, total_sigma)
        ([8, 4, 2, 1], [100, 100, 100, 50], [100, 50, 50, 20], 0.2, 1.732, 0.0),
        ([8, 4, 2, 1], [100, 100, 100, 50], [100, 50, 50, 20], 0.5, 1.732, 0.0),
        ([8, 4, 2, 1], [100, 100, 100, 50], [100, 50, 50, 20], 0.7, 1.732, 0.0),
        ([8, 4, 2, 1], [100, 100, 100, 50], [200, 200, 200, 20], 0.5, 1.732, 0.0),
        ([8, 4, 2, 1], [100, 100, 100, 50], [200, 200, 200, 20], 0.7, 1.732, 0.0),
        ([8, 4, 2, 1], [100, 100, 100, 50], [200, 200, 200, 20], 0.75, 1.732, 0.0),
        ([8, 4, 2, 1], [100, 100, 100, 50], [200, 200, 200, 20], 0.5, 3.0, 0.0),
        ([8, 4, 2, 1], [100, 100, 100, 50], [200, 200, 200, 20], 0.7, 3.0, 0.0),
        ([8, 4, 2, 1], [100, 100, 100, 50], [200, 200, 200, 20], 0.75, 3.0, 0.0),
        ([8, 4, 2, 1], [100, 100, 100, 50], [200, 200, 200, 20], 0.5, 1.5, 0.0),
        ([8, 4, 2, 1], [100, 100, 100, 50], [200, 200, 200, 20], 0.5, 2.0, 0.0),
        ([8, 4, 2, 1], [100, 100, 100, 50], [200, 200, 200, 20], 0.5, 1.732, 0.5),
        ([8, 4, 2, 1], [100, 100, 100, 50], [200, 200, 200, 20], 0.5, 1.732, 1.0),
        ([4, 2, 1], [100, 100, 50], [200, 200, 20], 0.5, 1.732, 0.0),
    ]
    
    print(f"Running search over {len(configs)} configurations...")
    best_config = None
    best_mean_dice = 0.0
    
    for idx, (levels, reg_its, aff_its, grad_step, flow_sigma, total_sigma) in enumerate(configs):
        print(f"\n--- Config {idx+1}/{len(configs)}: levels={levels}, reg_its={reg_its}, aff_its={aff_its}, grad_step={grad_step}, flow_sigma={flow_sigma}, total_sigma={total_sigma} ---")
        try:
            dices = []
            for name1, name2, f, m in pairs:
                # Adjust levels lengths of reg_its and aff_its if needed
                curr_reg_its = reg_its[:len(levels)]
                curr_aff_its = aff_its[:len(levels)]
                
                res = registration(
                    fixed=f,
                    moving=m,
                    type_of_transform='SyNTo',
                    backend='pytorch',
                    levels=levels,
                    reg_iterations=curr_reg_its,
                    affine_iterations=curr_aff_its,
                    grad_step=grad_step,
                    flow_sigma=flow_sigma,
                    total_sigma=total_sigma,
                    syn_metric='lncc',
                    syn_sampling=4,
                    mattes_bins=32
                )
                dice = compute_tissue_overlap(f, res['warpedmovout'])
                dices.append(dice)
                # Cleanup temp files generated by registration
                for path in res['fwdtransforms'] + res['invtransforms']:
                    if os.path.exists(path):
                        os.remove(path)
                
            mean_dice = np.mean(dices)
            print(f"Mean Dice: {mean_dice:.4f} (vs ANTs: {mean_ants_dice:.4f}, diff: {mean_dice - mean_ants_dice:.4f})")
            if mean_dice > best_mean_dice:
                best_mean_dice = mean_dice
                best_config = (levels, reg_its, aff_its, grad_step, flow_sigma, total_sigma)
        except Exception as e:
            print(f"Error during config execution: {e}")
            
    print("\n==============================================")
    print("Grid search finished!")
    print(f"Best configuration: {best_config}")
    print(f"Best Mean Dice: {best_mean_dice:.4f} (vs ANTs: {mean_ants_dice:.4f})")
    print("==============================================")

if __name__ == "__main__":
    main()
